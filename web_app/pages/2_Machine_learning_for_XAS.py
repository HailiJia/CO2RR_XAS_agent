def _minmax(y: np.ndarray) -> np.ndarray:
    span = float(np.max(y) - np.min(y))
    return np.zeros_like(y) if span == 0 else (y - float(np.min(y))) / span


def _area_norm(x: np.ndarray, y: np.ndarray) -> np.ndarray:
    area = float(np.trapz(np.abs(y), x))
    return y.astype(float) if area == 0 else y / area


def _target_value(row: Dict[str, Any], path: str) -> Any:
    if path.startswith(("descriptors.", "series.", "derived.")):
        return (row.get("metadata") or {}).get(path)
    value = _get_path(row.get("record") or {}, path)
    return value if value is not None else (row.get("metadata") or {}).get(path)


def _candidate_label_fields(rows: Sequence[Dict[str, Any]]) -> List[str]:
    preferred = ["sample.material.formula", "sample.material.name", "derived.absorber", "derived.edge", "derived.edge_group", "sample.ml_labels.adsorbate_identity", "sample.ml_labels.adsorption_motif", "sample.ml_labels.oxidation_coordination_class", "system.technique", "source_type", "record_domain"]
    keys = set()
    for row in rows:
        for key, value in (row.get("metadata") or {}).items():
            if value is not None and not (isinstance(value, str) and len(value) > 160):
                keys.add(key)
    return [key for key in preferred if key in keys] + sorted(keys - set(preferred))


def _common_grid(rows: Sequence[Dict[str, Any]], n_grid: int) -> np.ndarray:
    xmin = max(float(np.min(row["x"])) for row in rows if row.get("x") is not None)
    xmax = min(float(np.max(row["x"])) for row in rows if row.get("x") is not None)
    if xmin >= xmax:
        xmin = min(float(np.min(row["x"])) for row in rows if row.get("x") is not None)
        xmax = max(float(np.max(row["x"])) for row in rows if row.get("x") is not None)
    return np.linspace(xmin, xmax, int(n_grid))


def _build_feature_matrix(rows: Sequence[Dict[str, Any]], target_path: str, feature_kinds: Sequence[str], normalization: str, n_grid: int) -> Tuple[np.ndarray, np.ndarray, List[str], np.ndarray]:
    valid = [row for row in rows if row.get("x") is not None and row.get("y") is not None and row.get("n_points", 0) >= 5 and _target_value(row, target_path) not in [None, ""]]
    if len(valid) < 2:
        raise ValueError("Need at least two labeled spectra with valid x/y arrays.")
    grid = _common_grid(valid, n_grid)
    X, targets, feature_names = [], [], []
    for row in valid:
        order = np.argsort(row["x"])
        x, y = np.asarray(row["x"])[order], np.asarray(row["y"])[order]
        yi = np.interp(grid, x, y)
        if normalization == "minmax": yi = _minmax(yi)
        elif normalization == "area": yi = _area_norm(grid, yi)
        elif normalization == "zscore": yi = (yi - float(np.mean(yi))) / (float(np.std(yi)) or 1.0)
        feats, names = [], []
        if "raw" in feature_kinds:
            feats += yi.tolist(); names += [f"raw_{i:03d}" for i in range(len(grid))]
        if "derivative" in feature_kinds:
            dy = np.gradient(yi, grid); feats += dy.tolist(); names += [f"d1_{i:03d}" for i in range(len(grid))]
        if "cdf" in feature_kinds:
            cdf = np.cumsum(np.maximum(yi, 0)); cdf = cdf / (float(cdf[-1]) or 1.0); feats += cdf.tolist(); names += [f"cdf_{i:03d}" for i in range(len(grid))]
        if "peak_window" in feature_kinds:
            dy = np.gradient(yi, grid); peak_idx = int(np.argmax(yi)); vals = [float(grid[peak_idx]), float(yi[peak_idx]), float(np.trapz(yi, grid)), float(np.max(dy)), float(np.min(dy))]
            for idx in np.array_split(np.arange(len(grid)), 4): vals.append(float(np.trapz(yi[idx], grid[idx])) if len(idx) > 1 else 0.0)
            feats += vals; names += ["peak_x", "peak_y", "area", "derivative_max", "derivative_min", "window_1_area", "window_2_area", "window_3_area", "window_4_area"]
        if not feature_names: feature_names = names
        X.append(feats); targets.append(_target_value(row, target_path))
    return np.asarray(X, dtype=float), np.asarray(targets, dtype=object), feature_names, grid


def _make_model(task: str, model_name: str, use_pca: bool, n_components: int):
    if task == "classification":
        model = {"Logistic regression": LogisticRegression(max_iter=2000, class_weight="balanced"), "Random forest": RandomForestClassifier(n_estimators=300, random_state=7, class_weight="balanced"), "SVM": SVC(kernel="rbf", C=10.0, gamma="scale", class_weight="balanced")}[model_name]
    else:
        model = {"Ridge": Ridge(alpha=1.0), "Random forest": RandomForestRegressor(n_estimators=300, random_state=7), "SVR": SVR(kernel="rbf", C=10.0, gamma="scale")}[model_name]
    steps = [("scale", StandardScaler())]
    if use_pca: steps.append(("pca", PCA(n_components=max(1, int(n_components)), random_state=7)))
    steps.append(("model", model))
    return Pipeline(steps)


def _make_cv(task: str, y: np.ndarray):
    if task == "classification":
        labels, counts = np.unique(y.astype(str), return_counts=True)
        if len(labels) < 2 or int(np.min(counts)) < 2: raise ValueError("Auto-advisor needs at least two examples in each class.")
        return StratifiedKFold(n_splits=min(5, int(np.min(counts)), len(y)), shuffle=True, random_state=7)
    return KFold(n_splits=min(5, len(y)), shuffle=True, random_state=7)


def _run_auto_advisor(rows: Sequence[Dict[str, Any]], target_path: str, task: str, n_grid: int, max_configs: int) -> Tuple[List[Dict[str, Any]], List[str]]:
    feature_sets = [["peak_window"], ["derivative", "peak_window"], ["cdf", "peak_window"], ["raw"], ["raw", "peak_window"], ["raw", "derivative"], ["raw", "cdf"]]
    normalizations = ["minmax", "area", "zscore"]
    models = ["Logistic regression", "SVM", "Random forest"] if task == "classification" else ["Ridge", "SVR", "Random forest"]
    results, failures = [], []
    for feature_set in feature_sets:
        for norm in normalizations:
            try:
                X, y, _, _ = _build_feature_matrix(rows, target_path, feature_set, norm, n_grid)
                if task == "regression" and not all(_is_number(v) for v in y): raise ValueError("Regression target must be numeric.")
                y_model = y.astype(str) if task == "classification" else np.asarray([float(v) for v in y], dtype=float)
                cv = _make_cv(task, y)
            except Exception as exc:
                failures.append(f"{'+'.join(feature_set)} / {norm}: {exc}"); continue
            pca_options: List[Optional[int]] = [None] + [n for n in [2, 4, 8, 16] if n < min(X.shape)]
            for model_name in models:
                for n_comp in pca_options:
                    try:
                        model = _make_model(task, model_name, n_comp is not None, n_comp or 0)
                        scoring = "balanced_accuracy" if task == "classification" else "neg_root_mean_squared_error"
                        cvres = cross_validate(model, X, y_model, cv=cv, scoring=scoring, return_train_score=True, error_score=np.nan)
                        train_raw, valid_raw = float(np.nanmean(cvres["train_score"])), float(np.nanmean(cvres["test_score"]))
                        if task == "regression":
                            train_disp, valid_disp, score_name, gap, rank_score = -train_raw, -valid_raw, "validation_RMSE", -valid_raw - (-train_raw), valid_raw
                        else:
                            train_disp, valid_disp, score_name, gap, rank_score = train_raw, valid_raw, "validation_balanced_accuracy", train_raw - valid_raw, valid_raw
                        pca_label = "none" if n_comp is None else f"PCA({n_comp})"
                        advice = []
                        advice.append("uses dimension reduction" if n_comp is not None else "no dimension reduction")
                        if gap > 0.20: advice.append("possible overfitting")
                        if "peak_window" in feature_set and "raw" not in feature_set: advice.append("compact/interpretable features")
                        if "derivative" in feature_set: advice.append("edge-shift sensitive")
                        if "cdf" in feature_set: advice.append("smooth/integral line-shape")
                        results.append({"feature_set": "+".join(feature_set), "normalization": norm, "dimension_reduction": pca_label, "model": model_name, score_name: valid_disp, "train_score_same_metric": train_disp, "overfit_gap": gap, "rank_score": rank_score, "n_samples": int(X.shape[0]), "n_features": int(X.shape[1]), "advice": "; ".join(advice)})
                    except Exception as exc:
                        failures.append(f"{'+'.join(feature_set)} / {norm} / {model_name} / {n_comp}: {exc}")
    results = sorted(results, key=lambda item: (item["rank_score"], -abs(item.get("overfit_gap", 0.0))), reverse=True)
    for i, item in enumerate(results, 1): item["rank"] = i
    return results[:max_configs], failures


def _advisor_recommendation(results: Sequence[Dict[str, Any]], task: str) -> List[str]:
    if not results: return ["No valid configuration finished. Check labels, class balance, and record count."]
    best = results[0]; recs = [f"Recommended setting: **{best['feature_set']}** features, **{best['normalization']}** normalization, **{best['dimension_reduction']}**, **{best['model']}**."]
    recs.append("Use PCA/dimension reduction." if best["dimension_reduction"] != "none" else "Do not use PCA for now; the sweep did not require it for the top setting.")
    if "peak_window" in best["feature_set"]: recs.append("Keep peak/window features as an interpretable baseline.")
    if "derivative" in best["feature_set"]: recs.append("Derivative features are useful, so edge position/white-line shifts likely matter.")
    if "cdf" in best["feature_set"]: recs.append("CDF features are useful, so integrated line shape may be robust.")
    if best.get("overfit_gap", 0) > 0.20: recs.append("The train-validation gap is still large; add records or reduce feature dimension before trusting predictions.")
    recs.append("Use the top few rows as candidates; prefer a slightly lower score if it has fewer features and a smaller overfit gap.")
    return recs


def _train_manual(X: np.ndarray, y: np.ndarray, task: str, model_name: str, use_pca: bool, n_components: int) -> Dict[str, Any]:
    if len(y) < 4: raise ValueError("Need at least four labeled spectra for a train/test split.")
    model = _make_model(task, model_name, use_pca, n_components)
    if task == "classification":
        labels, counts = np.unique(y.astype(str), return_counts=True)
        strat = y.astype(str) if np.min(counts) >= 2 else None
        Xtr, Xte, ytr, yte = train_test_split(X, y.astype(str), test_size=0.25, random_state=7, stratify=strat)
        model.fit(Xtr, ytr); ptr, pte = model.predict(Xtr), model.predict(Xte)
        return {"metrics": {"train_balanced_accuracy": float(balanced_accuracy_score(ytr, ptr)), "test_balanced_accuracy": float(balanced_accuracy_score(yte, pte)), "test_macro_f1": float(f1_score(yte, pte, average="macro"))}, "prediction_table": [{"true": str(t), "predicted": str(p)} for t, p in zip(yte, pte)]}
    ynum = np.asarray([float(v) for v in y], dtype=float)
    Xtr, Xte, ytr, yte = train_test_split(X, ynum, test_size=0.25, random_state=7)
    model.fit(Xtr, ytr); ptr, pte = model.predict(Xtr), model.predict(Xte)
    return {"metrics": {"train_RMSE": float(mean_squared_error(ytr, ptr, squared=False)), "test_RMSE": float(mean_squared_error(yte, pte, squared=False)), "test_MAE": float(mean_absolute_error(yte, pte)), "test_R2": float(r2_score(yte, pte)) if len(yte) > 1 else float("nan")}, "prediction_table": [{"true": float(t), "predicted": float(p)} for t, p in zip(yte, pte)]}


