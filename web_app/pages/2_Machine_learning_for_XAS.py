def safe_str(x: Any) -> str:
    return "" if x is None else str(x)


def lower(x: Any) -> str:
    return safe_str(x).strip().lower()


def is_number(x: Any) -> bool:
    try:
        return math.isfinite(float(x))
    except Exception:
        return False


def integrate(y: np.ndarray, x: np.ndarray) -> float:
    if hasattr(np, "trapezoid"):
        return float(np.trapezoid(y, x))
    return float(np.trapz(y, x))


def arr(values: Any) -> Optional[np.ndarray]:
    if values is None:
        return None
    try:
        out = np.asarray(values, dtype=float)
    except Exception:
        return None
    return out if out.ndim == 1 and out.size and np.all(np.isfinite(out)) else None


def get_path(data: Dict[str, Any], dotted: str) -> Any:
    node: Any = data
    for part in dotted.split("."):
        if isinstance(node, dict) and part in node:
            node = node[part]
        else:
            return None
    return node


def flatten_scalars(data: Any, prefix: str = "", depth: int = 5) -> Dict[str, Any]:
    if depth < 0:
        return {}
    out: Dict[str, Any] = {}
    if isinstance(data, dict):
        for key, value in data.items():
            if str(key).startswith("_"):
                continue
            out.update(flatten_scalars(value, f"{prefix}.{key}" if prefix else str(key), depth - 1))
    elif isinstance(data, list):
        if len(data) <= 8 and all(not isinstance(v, (dict, list)) for v in data):
            out[prefix] = ", ".join(safe_str(v) for v in data)
    elif isinstance(data, (str, int, float, bool)) or data is None:
        out[prefix] = data
    return out


def find_first_key(data: Any, names: Sequence[str], depth: int = 6) -> Any:
    keys = {lower(k) for k in names}
    if depth < 0:
        return None
    if isinstance(data, dict):
        for k, v in data.items():
            if lower(k) in keys and not isinstance(v, (dict, list)):
                return v
        for v in data.values():
            found = find_first_key(v, names, depth - 1)
            if found not in [None, ""]:
                return found
    elif isinstance(data, list) and len(data) <= 30:
        for v in data:
            found = find_first_key(v, names, depth - 1)
            if found not in [None, ""]:
                return found
    return None


def descriptor_values(record: Dict[str, Any]) -> Dict[str, Any]:
    out: Dict[str, Any] = {}
    for output in (record.get("descriptors") or {}).get("outputs") or []:
        label = output.get("label") or output.get("name") or "descriptor_output"
        for desc in output.get("descriptors") or []:
            name = desc.get("name")
            if name:
                out[f"descriptors.{name}"] = desc.get("value")
                out[f"descriptors.{label}.{name}"] = desc.get("value")
    return out


def canonical_element(x: Any) -> str:
    toks = re.findall(r"[A-Za-z]{1,2}", safe_str(x))
    return (toks[0].upper() if len(toks[0]) == 1 else toks[0].capitalize()) if toks else ""


def canonical_edge(x: Any) -> str:
    text = safe_str(x).upper()
    m = re.search(r"L\s*([1-3])", text)
    if m:
        return "L" + m.group(1)
    m = re.search(r"M\s*([1-5])", text)
    if m:
        return "M" + m.group(1)
    m = re.search(r"\b(K|L|M|N|O)\b", text)
    return m.group(1) if m else ""


def infer_edge(x: Optional[np.ndarray]) -> Tuple[str, str, str]:
    if x is None or not x.size:
        return "unknown", "unknown", "not inferred"
    xmin, xmax, mid = float(np.min(x)), float(np.max(x)), float(0.5 * (np.min(x) + np.max(x)))
    for el, edge, lo, hi in EDGE_WINDOWS:
        if lo <= mid <= hi or xmin <= lo <= xmax or xmin <= hi <= xmax:
            return el, edge, "inferred from energy range"
    return "unknown", "unknown", "not inferred"


def absorber_edge(record: Dict[str, Any], x: Optional[np.ndarray]) -> Tuple[str, str, str]:
    raw_abs = get_path(record, "system.absorber") or get_path(record, "system.configuration.absorber") or get_path(record, "measurement.absorber") or find_first_key(record, ["absorber", "absorbing_element", "absorber_element"])
    raw_edge = get_path(record, "system.edge") or get_path(record, "system.configuration.edge") or get_path(record, "measurement.edge") or find_first_key(record, ["edge", "absorption_edge"])
    abs_el, edge = canonical_element(raw_abs), canonical_edge(raw_edge)
    if abs_el and edge:
        return abs_el, edge, "metadata"
    i_abs, i_edge, source = infer_edge(x)
    return abs_el or i_abs, edge or i_edge, source


def edge_group(absorber: str, edge: str) -> str:
    return "Unknown absorber/edge" if not absorber or absorber == "unknown" or not edge or edge == "unknown" else f"{absorber} {edge}-edge"


def classify_series(x_name: str, x_unit: str, y_name: str, y_unit: str) -> Tuple[bool, str, str]:
    xn, xu, yn = lower(x_name), lower(x_unit), lower(y_name)
    if xn in {"q", "qz"} or "angstrom^-1" in xu or "1/angstrom" in xu:
        return False, "not_xas_scattering", "x-axis is q/scattering vector"
    has_energy_x = any(k in xn for k in ENERGY_NAMES) or xn in {"e", "energy_ev"}
    has_energy_unit = any(u == xu or u in xu for u in ENERGY_UNITS)
    has_signal = any(k in yn for k in SIGNAL_NAMES)
    if has_energy_x and has_energy_unit and has_signal:
        return True, "xas_like", "energy-axis XAS/XANES-like spectrum"
    if has_energy_x and has_energy_unit:
        return True, "xas_like_uncertain_y", "energy-axis spectrum; y name is not standard XAS"
    return False, "not_xas_unknown_axis", "no incident-energy x-axis with eV/keV units"


def make_row(source: str, record: Dict[str, Any], series_id: str, x: Optional[np.ndarray], y: Optional[np.ndarray], x_name: str, x_unit: str, y_name: str, y_unit: str) -> Dict[str, Any]:
    absorber, edge, edge_source = absorber_edge(record, x)
    group = edge_group(absorber, edge)
    ok, classification, reason = (False, "not_plottable_invalid_xy", "missing/non-numeric x or y, or length mismatch") if x is None or y is None or x.size != y.size else classify_series(x_name, x_unit, y_name, y_unit)
    metadata = {**flatten_scalars(record), **descriptor_values(record), "series.series_id": series_id, "series.x_name": x_name, "series.x_unit": x_unit, "series.y_name": y_name, "series.y_unit": y_unit, "derived.absorber": absorber, "derived.edge": edge, "derived.edge_group": group, "derived.n_points": int(x.size if x is not None else 0)}
    return {"source_name": source, "record_id": record.get("record_id"), "series_id": series_id, "technique": get_path(record, "system.technique") or get_path(record, "system.configuration.technique"), "formula": get_path(record, "sample.material.formula") or get_path(record, "sample.material.name"), "absorber": absorber, "edge": edge, "edge_group": group, "edge_source": edge_source, "x_name": x_name, "x_unit": x_unit, "y_name": y_name, "y_unit": y_unit, "n_points": int(x.size if x is not None else 0), "is_xas": ok, "classification": classification, "reason": reason, "record": record, "metadata": metadata, "x": x, "y": y}


def read_records(files: Sequence[Any]) -> List[Tuple[str, Dict[str, Any]]]:
    records = []
    for uploaded in files or []:
        name, payload = uploaded.name, uploaded.getvalue()
        if name.lower().endswith(".zip"):
            with zipfile.ZipFile(io.BytesIO(payload)) as zf:
                for member in sorted(zf.namelist()):
                    if member.lower().endswith(".json"):
                        try:
                            data = json.loads(zf.read(member).decode("utf-8"))
                            if isinstance(data, dict):
                                records.append((member, data))
                        except Exception:
                            pass
        elif name.lower().endswith(".json"):
            data = json.loads(payload.decode("utf-8"))
            if isinstance(data, dict):
                records.append((name, data))
    return records


def rows_from_records(records: Sequence[Tuple[str, Dict[str, Any]]]) -> List[Dict[str, Any]]:
    rows = []
    for source, record in records:
        added = False
        for i, series in enumerate((record.get("measurement") or {}).get("series") or []):
            xobj = (series.get("independent_variables") or [{}])[0]
            channels = series.get("channels") or []
            yobj = next((c for c in channels if lower(c.get("role")) == "primary_signal"), channels[0] if channels else {})
            rows.append(make_row(source, record, safe_str(series.get("series_id")) or f"series_{i}", arr(xobj.get("values")), arr(yobj.get("values")), safe_str(xobj.get("name")), safe_str(xobj.get("unit")), safe_str(yobj.get("name")), safe_str(yobj.get("unit"))))
            added = True
        for i, out in enumerate((record.get("descriptors") or {}).get("outputs") or []):
            x = arr(out.get("energy") or out.get("energy_eV") or out.get("x") or out.get("grid"))
            y = arr(out.get("mu") or out.get("absorption") or out.get("intensity") or out.get("y") or out.get("values"))
            if x is not None and y is not None and x.size == y.size:
                rows.append(make_row(source, record, safe_str(out.get("label") or out.get("name") or f"descriptor_output_{i}"), x, y, "energy", "eV", safe_str(out.get("channel") or out.get("name") or "absorption"), safe_str(out.get("unit") or "a.u.")))
                added = True
        if not added:
            rows.append(make_row(source, record, "", None, None, "", "", "", ""))
    return rows


def summary(rows: Sequence[Dict[str, Any]]) -> List[Dict[str, Any]]:
    keys = ["source_name", "record_id", "series_id", "technique", "formula", "absorber", "edge", "edge_group", "edge_source", "x_name", "x_unit", "y_name", "y_unit", "n_points", "is_xas", "classification", "reason"]
    return [{k: r.get(k) for k in keys} for r in rows]


def to_csv(rows: Sequence[Dict[str, Any]]) -> str:
    if not rows:
        return ""
    out = io.StringIO()
    writer = csv.DictWriter(out, fieldnames=list(rows[0].keys()))
    writer.writeheader()
    writer.writerows(rows)
    return out.getvalue()


def minmax(y: np.ndarray) -> np.ndarray:
    span = float(np.max(y) - np.min(y))
    return np.zeros_like(y) if span == 0 else (y - float(np.min(y))) / span


def target_value(row: Dict[str, Any], path: str) -> Any:
    return (row.get("metadata") or {}).get(path) if path.startswith(("derived.", "series.", "descriptors.")) else (get_path(row.get("record") or {}, path) or (row.get("metadata") or {}).get(path))


def labels(rows: Sequence[Dict[str, Any]]) -> List[str]:
    preferred = ["sample.material.formula", "sample.material.name", "derived.absorber", "derived.edge", "derived.edge_group", "system.technique", "source_type", "record_domain"]
    keys = set()
    for r in rows:
        for k, v in (r.get("metadata") or {}).items():
            if v is not None and not (isinstance(v, str) and len(v) > 160):
                keys.add(k)
    return [k for k in preferred if k in keys] + sorted(keys - set(preferred))


def feature_matrix(rows: Sequence[Dict[str, Any]], target: str, kinds: Sequence[str], norm: str, n_grid: int):
    valid = [r for r in rows if r.get("x") is not None and r.get("y") is not None and r.get("n_points", 0) >= 5 and target_value(r, target) not in [None, ""]]
    if len(valid) < 2:
        raise ValueError("Need at least two labeled spectra with valid x/y arrays.")
    xmin = max(float(np.min(r["x"])) for r in valid)
    xmax = min(float(np.max(r["x"])) for r in valid)
    if xmin >= xmax:
        xmin = min(float(np.min(r["x"])) for r in valid)
        xmax = max(float(np.max(r["x"])) for r in valid)
    grid = np.linspace(xmin, xmax, int(n_grid))
    X=[]; y=[]; names=[]
    for r in valid:
        order = np.argsort(r["x"])
        xi, yi = np.asarray(r["x"])[order], np.asarray(r["y"])[order]
        yi = np.interp(grid, xi, yi)
        if norm == "minmax":
            yi = minmax(yi)
        elif norm == "area":
            yi = yi / (abs(integrate(np.abs(yi), grid)) or 1.0)
        elif norm == "zscore":
            yi = (yi - float(np.mean(yi))) / (float(np.std(yi)) or 1.0)
        feats=[]; local=[]
        if "raw" in kinds:
            feats += yi.tolist(); local += [f"raw_{i:03d}" for i in range(len(grid))]
        if "derivative" in kinds:
            dy = np.gradient(yi, grid); feats += dy.tolist(); local += [f"d1_{i:03d}" for i in range(len(grid))]
        if "cdf" in kinds:
            cdf = np.cumsum(np.maximum(yi,0)); cdf = cdf/(float(cdf[-1]) or 1.0); feats += cdf.tolist(); local += [f"cdf_{i:03d}" for i in range(len(grid))]
        if "peak_window" in kinds:
            dy = np.gradient(yi, grid); pk = int(np.argmax(yi)); vals = [float(grid[pk]), float(yi[pk]), integrate(yi, grid), float(np.max(dy)), float(np.min(dy))]
            vals += [integrate(yi[idx], grid[idx]) if len(idx)>1 else 0.0 for idx in np.array_split(np.arange(len(grid)),4)]
            feats += vals; local += ["peak_x","peak_y","area","derivative_max","derivative_min","window_1_area","window_2_area","window_3_area","window_4_area"]
        if not names:
            names = local
        X.append(feats); y.append(target_value(r, target))
    return np.asarray(X, dtype=float), np.asarray(y, dtype=object), names, grid


def make_model(task: str, name: str, use_pca: bool, n_comp: int):
    if task == "classification":
        models = {"Dummy baseline": DummyClassifier(strategy="most_frequent"), "Logistic regression": LogisticRegression(max_iter=2000, class_weight="balanced"), "Random forest": RandomForestClassifier(n_estimators=300, random_state=7, class_weight="balanced"), "SVM": SVC(kernel="rbf", C=10.0, gamma="scale", class_weight="balanced")}
    else:
        models = {"Dummy baseline": DummyRegressor(strategy="mean"), "Ridge": Ridge(alpha=1.0), "Random forest": RandomForestRegressor(n_estimators=300, random_state=7), "SVR": SVR(kernel="rbf", C=10.0, gamma="scale")}
    steps = [("scale", StandardScaler())]
    if use_pca:
        steps.append(("pca", PCA(n_components=max(1, int(n_comp)), random_state=7)))
    steps.append(("model", models[name]))
    return Pipeline(steps)


def cv_for(task: str, y: np.ndarray):
    if len(y) < 4:
        return None
    if task == "classification":
        _, counts = np.unique(y.astype(str), return_counts=True)
        if len(counts) >= 2 and int(np.min(counts)) >= 2:
            return StratifiedKFold(n_splits=min(5, int(np.min(counts)), len(y)), shuffle=True, random_state=7)
    return KFold(n_splits=min(5, len(y)), shuffle=True, random_state=7)


def evaluate(X: np.ndarray, y: np.ndarray, task: str, model_name: str, use_pca=False, n_comp=0) -> Dict[str, Any]:
    if task == "classification":
        ym = y.astype(str)
        unique = np.unique(ym)
        if len(unique) < 2 and model_name != "Dummy baseline":
            raise ValueError("Only one class; non-dummy classifiers require at least two classes.")
        scoring = "balanced_accuracy"
    else:
        if not all(is_number(v) for v in y):
            raise ValueError("Regression target must be numeric.")
        ym = np.asarray([float(v) for v in y], dtype=float)
        scoring = "neg_root_mean_squared_error"
    model = make_model(task, model_name, use_pca, n_comp)
    cv = cv_for(task, ym)
    if cv is None:
        model.fit(X, ym)
        pred = model.predict(X)
        if task == "classification":
            score = float(balanced_accuracy_score(ym, pred))
            return {"validation": score, "train": score, "gap": 0.0, "rank_score": score, "metric": "validation_balanced_accuracy", "evaluation": "train-only (<4 samples)"}
        rmse = float(np.sqrt(mean_squared_error(ym, pred)))
        return {"validation": rmse, "train": rmse, "gap": 0.0, "rank_score": -rmse, "metric": "validation_RMSE", "evaluation": "train-only (<4 samples)"}
    res = cross_validate(model, X, ym, cv=cv, scoring=scoring, return_train_score=True, error_score=np.nan)
    tr, va = float(np.nanmean(res["train_score"])), float(np.nanmean(res["test_score"]))
    if not math.isfinite(tr) or not math.isfinite(va):
        raise ValueError("non-finite CV score")
    if task == "classification":
        return {"validation": va, "train": tr, "gap": tr-va, "rank_score": va, "metric": "validation_balanced_accuracy", "evaluation": "cross-validation"}
    return {"validation": -va, "train": -tr, "gap": -va-(-tr), "rank_score": va, "metric": "validation_RMSE", "evaluation": "cross-validation"}


def auto_advisor(rows, target, task, n_grid, max_rows):
    feature_sets = [["peak_window"],["raw","peak_window"],["derivative","peak_window"],["cdf","peak_window"],["raw"],["raw","derivative"],["raw","cdf"]]
    models = ["Dummy baseline", "Logistic regression", "SVM", "Random forest"] if task == "classification" else ["Dummy baseline", "Ridge", "SVR", "Random forest"]
    results=[]; failures=[]
    for fs in feature_sets:
        for norm in ["minmax","area","zscore"]:
            try:
                X,y,_,_ = feature_matrix(rows, target, fs, norm, n_grid)
            except Exception as exc:
                failures.append(f"{'+'.join(fs)} / {norm}: {exc}")
                continue
            for model in models:
                for n_comp in [None] + [n for n in [2,4,8,16] if n < min(X.shape)]:
                    try:
                        m = evaluate(X, y, task, model, n_comp is not None, n_comp or 0)
                        item = {"feature_set":"+".join(fs),"normalization":norm,"dimension_reduction":"none" if n_comp is None else f"PCA({n_comp})","model":model,m["metric"]:m["validation"],"train_score_same_metric":m["train"],"overfit_gap":m["gap"],"rank_score":m["rank_score"],"evaluation":m["evaluation"],"n_samples":int(X.shape[0]),"n_features":int(X.shape[1]),"advice":"; ".join(["baseline only" if model=="Dummy baseline" else "model", "compact" if "peak_window" in fs else "full-grid"])}
                        results.append(item)
                    except Exception as exc:
                        failures.append(f"{'+'.join(fs)} / {norm} / {model} / {n_comp}: {exc}")
    results = sorted(results, key=lambda r: (r["rank_score"], -abs(r.get("overfit_gap",0))), reverse=True)
    for i,r in enumerate(results,1):
        r["rank"] = i
    return results[:max_rows], failures


def train_manual(X, y, task, model_name, use_pca, n_comp):
    if task == "classification":
        ym = y.astype(str)
        labels, counts = np.unique(ym, return_counts=True)
        if len(labels) < 2:
            model_name = "Dummy baseline"
        model = make_model(task, model_name, use_pca, n_comp)
        if len(ym) < 4:
            model.fit(X, ym)
            pred = model.predict(X)
            return {"metrics":{"train_balanced_accuracy":float(balanced_accuracy_score(ym,pred)),"note":"train-only; fewer than four spectra"},"table":[{"true":str(t),"predicted":str(p)} for t,p in zip(ym,pred)]}
        strat = ym if len(labels) >= 2 and np.min(counts) >= 2 else None
        Xtr,Xte,ytr,yte = train_test_split(X, ym, test_size=0.25, random_state=7, stratify=strat)
        model.fit(Xtr,ytr)
        ptr,pte = model.predict(Xtr),model.predict(Xte)
        return {"metrics":{"train_balanced_accuracy":float(balanced_accuracy_score(ytr,ptr)),"test_balanced_accuracy":float(balanced_accuracy_score(yte,pte)),"test_macro_f1":float(f1_score(yte,pte,average="macro"))},"table":[{"true":str(t),"predicted":str(p)} for t,p in zip(yte,pte)]}
    if not all(is_number(v) for v in y):
        raise ValueError("Regression target must be numeric.")
    ym = np.asarray([float(v) for v in y], dtype=float)
    model = make_model(task, model_name, use_pca, n_comp)
    if len(ym) < 4:
        model.fit(X, ym)
        pred = model.predict(X)
        return {"metrics":{"train_RMSE":float(np.sqrt(mean_squared_error(ym,pred))),"train_MAE":float(mean_absolute_error(ym,pred)),"note":"train-only; fewer than four spectra"},"table":[{"true":float(t),"predicted":float(p)} for t,p in zip(ym,pred)]}
    Xtr,Xte,ytr,yte = train_test_split(X, ym, test_size=0.25, random_state=7)
    model.fit(Xtr,ytr)
    ptr,pte = model.predict(Xtr),model.predict(Xte)
    return {"metrics":{"train_RMSE":float(np.sqrt(mean_squared_error(ytr,ptr))),"test_RMSE":float(np.sqrt(mean_squared_error(yte,pte))),"test_MAE":float(mean_absolute_error(yte,pte)),"test_R2":float(r2_score(yte,pte)) if len(yte)>1 else float("nan")},"table":[{"true":float(t),"predicted":float(p)} for t,p in zip(yte,pte)]}

