def _safe_str(value: Any) -> str:
    return "" if value is None else str(value)


def _lower(value: Any) -> str:
    return _safe_str(value).strip().lower()


def _is_number(value: Any) -> bool:
    try:
        return math.isfinite(float(value))
    except Exception:
        return False


def _as_float_array(values: Any) -> Optional[np.ndarray]:
    if not isinstance(values, list) or not values:
        return None
    try:
        arr = np.asarray(values, dtype=float)
    except Exception:
        return None
    return arr if arr.ndim == 1 and arr.size and np.all(np.isfinite(arr)) else None


def _get_path(data: Dict[str, Any], dotted_path: str) -> Any:
    node: Any = data
    for part in dotted_path.split("."):
        if isinstance(node, dict) and part in node:
            node = node[part]
        else:
            return None
    return node


def _flatten_scalar_paths(data: Any, prefix: str = "", max_depth: int = 5) -> Dict[str, Any]:
    if max_depth < 0:
        return {}
    out: Dict[str, Any] = {}
    if isinstance(data, dict):
        for key, value in data.items():
            if str(key).startswith("_"):
                continue
            child = f"{prefix}.{key}" if prefix else str(key)
            out.update(_flatten_scalar_paths(value, child, max_depth - 1))
    elif isinstance(data, list):
        if len(data) <= 8 and all(not isinstance(v, (dict, list)) for v in data):
            out[prefix] = ", ".join(_safe_str(v) for v in data)
    elif isinstance(data, (str, int, float, bool)) or data is None:
        out[prefix] = data
    return out


def _descriptor_values(record: Dict[str, Any]) -> Dict[str, Any]:
    out: Dict[str, Any] = {}
    for output in (record.get("descriptors") or {}).get("outputs") or []:
        label = output.get("label") or "descriptor_output"
        for desc in output.get("descriptors") or []:
            name = desc.get("name")
            if name:
                out[f"descriptors.{name}"] = desc.get("value")
                out[f"descriptors.{label}.{name}"] = desc.get("value")
    return out


def _find_first_key(data: Any, key_names: Sequence[str], max_depth: int = 6) -> Any:
    key_set = {_lower(k) for k in key_names}
    if max_depth < 0:
        return None
    if isinstance(data, dict):
        for key, value in data.items():
            if _lower(key) in key_set and not isinstance(value, (dict, list)):
                return value
        for value in data.values():
            found = _find_first_key(value, key_names, max_depth - 1)
            if found not in [None, ""]:
                return found
    elif isinstance(data, list) and len(data) <= 20:
        for value in data:
            found = _find_first_key(value, key_names, max_depth - 1)
            if found not in [None, ""]:
                return found
    return None


def _material_formula(record: Dict[str, Any]) -> str:
    return _safe_str(_get_path(record, "sample.material.formula")) or _safe_str(_get_path(record, "sample.material.name"))


def _canonicalize_absorber(raw_absorber: Any) -> str:
    text = _safe_str(raw_absorber).strip()
    if not text:
        return ""
    cleaned = re.sub(r"[^A-Za-z]", " ", text)
    tokens = [tok for tok in cleaned.split() if tok]
    if not tokens:
        return text
    token = tokens[0]
    return token.upper() if len(token) == 1 else token[0].upper() + token[1:].lower()


def _parse_absorber_from_edge_string(raw_edge: Any) -> str:
    match = re.match(r"^\s*([A-Za-z]{1,2})[\s_\-]", _safe_str(raw_edge).strip())
    return _canonicalize_absorber(match.group(1)) if match else ""


def _canonicalize_edge(raw_edge: Any) -> str:
    text = _safe_str(raw_edge).strip()
    if not text:
        return ""
    upper = text.upper()
    normalized = re.sub(r"[^A-Z0-9]+", " ", upper).replace(" EDGE", " ").replace("EDGE ", " ").strip()
    for pattern, prefix in [(r"\bL\s*([1-3])\b", "L"), (r"\bM\s*([1-5])\b", "M"), (r"\bN\s*([1-7])\b", "N"), (r"\bO\s*([1-7])\b", "O")]:
        match = re.search(pattern, normalized)
        if match:
            return f"{prefix}{match.group(1)}"
    for token in ["K", "L", "M", "N", "O"]:
        if re.search(rf"\b{token}\b", normalized):
            return token
    match = re.search(r"(L[1-3]|M[1-5]|N[1-7]|O[1-7]|K|L|M|N|O)", upper)
    return match.group(1) if match else text.strip().upper()


def _infer_absorber_edge_from_energy(x: Optional[np.ndarray]) -> Tuple[str, str, str]:
    if x is None or not x.size:
        return "unknown", "unknown", "not inferred"
    xmin, xmax = float(np.nanmin(x)), float(np.nanmax(x))
    xmid = 0.5 * (xmin + xmax)
    for absorber, edge, lo, hi in EDGE_ENERGY_WINDOWS:
        if (lo <= xmid <= hi) or (xmin <= lo <= xmax) or (xmin <= hi <= xmax):
            return absorber, edge, "inferred from energy range"
    return "unknown", "unknown", "not inferred"


def _resolve_absorber_edge(record: Dict[str, Any], x: Optional[np.ndarray]) -> Tuple[str, str, str]:
    raw_absorber = (_get_path(record, "system.absorber") or _get_path(record, "system.configuration.absorber") or _get_path(record, "measurement.absorber") or _find_first_key(record, ["absorber", "absorbing_element", "absorber_element", "element"]))
    raw_edge = (_get_path(record, "system.edge") or _get_path(record, "system.configuration.edge") or _get_path(record, "measurement.edge") or _find_first_key(record, ["edge", "absorption_edge"]))
    absorber = _canonicalize_absorber(raw_absorber) or _parse_absorber_from_edge_string(raw_edge)
    edge = _canonicalize_edge(raw_edge)
    if absorber and edge:
        return absorber, edge, "metadata"
    inferred_abs, inferred_edge, source = _infer_absorber_edge_from_energy(x)
    return absorber or inferred_abs or "unknown", edge or inferred_edge or "unknown", source


def _edge_group(absorber: str, edge: str) -> str:
    absorber_c = _canonicalize_absorber(absorber)
    edge_c = _canonicalize_edge(edge)
    if not absorber_c or absorber_c.lower() == "unknown" or not edge_c or edge_c == "UNKNOWN":
        return "Unknown absorber/edge"
    return f"{absorber_c} {edge_c}-edge"


